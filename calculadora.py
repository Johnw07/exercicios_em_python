numero1= int(input("Infrome o primeiro número: "))
numero2= int(input("Informe o segundo número: "))

print('{} + {}='.format(numero1, numero2))
print(numero1 + numero2)

print('{} - {}= '.format(numero1, numero2))
print(numero1 - numero2)

print('{} * {}= '.format(numero1, numero2))
print(numero1 * numero2)

print(' {} / {}= '.format(numero1, numero2))
print(numero1 / numero2)